    private Categorie getParam() {
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jComboBox1.getSelectedIndex() == 0) {
            return null;
        }
        Categorie categorie;
        categorie = new Categorie(new Integer(jTextField1.getText()), jTextField2.getText());
        categorie.setClasse(classes.get(jComboBox1.getSelectedIndex() - 1));
        return categorie;
    }

    private void initClasse() {
        jComboBox1.addItem("--choisir--");
        for (int i = 0; i < classes.size(); i++) {
            Classe classe = classes.get(i);
            jComboBox1.addItem("(" + classe.getNum() + ") " + classe.getLibelle());
        }
    }

