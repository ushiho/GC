    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Categorie categorie = getParam();
        if (categorie == null) {
            JOptionPane.showMessageDialog(null, "Tous les champs sont n√©cissaires", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            int res = categorieServices.addCategorie(categorie.getNum(), categorie.getLibelle(), categorie.getClasse().getNum());
            if (res < 0) {
                JOptionPane.showMessageDialog(null, "Cette libelle ou cet numero existe d√©ja pour une autre categorie", "Info", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Cette categorie est bien ajout√©", "Resultat", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jComboBox1.setSelectedIndex(0);
                this.hide();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

