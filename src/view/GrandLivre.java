    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        if (jComboBox3.getSelectedIndex() != 0) {
            jComboBox2.removeAllItems();
            jComboBox1.removeAllItems();
            String num = "";
            jComboBox1.addItem("--choisir--");
            jComboBox2.addItem("--choisir--");
            for (int i = 0; i < comptes.size(); i++) {
                Compte compte = comptes.get(i);
                num = "(" + compte.getCategorie().getClasse().getNum() + ""
                        + compte.getCategorie().getNum() + "" + compte.getNum() + ") ";
                jComboBox1.addItem(num + "" + compte.getLibelle());
                jComboBox2.addItem(num + "" + compte.getLibelle());
            }
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Entreprise entrepriseSelected = entreprises.get(jComboBox3.getSelectedIndex() - 1);
        Compte compteDep = comptes.get(jComboBox1.getSelectedIndex()-1);
        Compte compteArr = comptes.get(jComboBox2.getSelectedIndex()-1);
        Date dateDep = jDateChooser1.getDate() ;
        Date dateArr = jDateChooser2.getDate() ;
        List<GLivre> lignes = gLivreService.DataAsListe(compteTrService.findDataForGLivre(
                entrepriseSelected.getId(),dateDep,dateArr,compteDep,compteArr)) ;
        Map paraMap = new HashMap();
        paraMap.put("entreprise", entrepriseSelected.getNumSecuriteSiege());
        paraMap.put("numDep", compteDep.getCategorie().getClasse().getNum()
                + "" + compteDep.getCategorie().getNum() + "" + compteDep.getNum());
        paraMap.put("numArr", compteArr.getCategorie().getClasse().getNum()
                + "" + compteArr.getCategorie().getNum() + "" + compteArr.getNum());
        paraMap.put("dateDep", jDateChooser1.getDate());
        paraMap.put("dateArr", jDateChooser2.getDate());
        try {
            JasperUtil.generatePdf(lignes, paraMap, true);
        } catch (JRException | IOException ex) {
            Logger.getLogger(GrandLivre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

