    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedIndex() != 0) {
            jComboBox2.removeAllItems();
            Classe classeSelected = classes.get(jComboBox1.getSelectedIndex() - 1);
            categories = categorieServices.findCategoriesByCriteria(-1, null, classeSelected.getNum());
            jComboBox2.addItem("--choisir--");
            for (int i = 0; i < categories.size(); i++) {
                Categorie categorie = categories.get(i);
                jComboBox2.addItem("(" + classeSelected.getNum() + "" + categorie.getNum() + ") " + categorie.getLibelle());
            }

        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

