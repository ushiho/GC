    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Compte compte = getParam();
        if(compte==null){
            JOptionPane.showMessageDialog(null, "Rien à ajouter, tous les cases sont nécissaires", "alert", JOptionPane.WARNING_MESSAGE);
        }else{
        int res = compteServices.addCompte(compte.getNum(), compte.getLibelle(), compte.getCategorie().getNum());
        if (res < 0) {
            JOptionPane.showMessageDialog(null, "Cette libelle ou cet numero existe déja pour un compte", "Info", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Ce Compte est bien ajouté", "Resultat", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.removeAllItems();
            this.hide();
        }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

