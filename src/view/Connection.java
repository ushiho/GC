    private Utilisateur getParam() {
        int poste = 0;
        Utilisateur user = new Utilisateur();
        user.setLogin(jTextField1.getText());
        user.setPassword(jPasswordField1.getText());
        if (jComboBox1.getSelectedIndex() == 2) {
            poste = 2;
        }
        if (jComboBox1.getSelectedIndex() == 1) {
            poste = 1;
        }
        user.setPoste(poste);
        return user;
    }
