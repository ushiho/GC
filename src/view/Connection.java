    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Utilisateur user = getParam();
        Utilisateur loadedUser = userServices.seConnecter(user);
        if (loadedUser == null) {
            JOptionPane.showMessageDialog(null, "Erreur : le mot de passe, le login ou le poste sont incorrects ! Essayer de nouveau", "Resultat", JOptionPane.ERROR_MESSAGE);
        } else {
            Session.updateAttribute(loadedUser, "utilisateur");
            JOptionPane.showMessageDialog(null, "Bienvenue " + loadedUser.getLogin(), "Resultat", JOptionPane.INFORMATION_MESSAGE);
            new Transaction().setVisible(true);
            this.hide();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

