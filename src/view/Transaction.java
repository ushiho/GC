    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<DataTable> donnesWithoutLastLigne = donnes;
        donnesWithoutLastLigne.remove(donnes.size() - 1);
        compteTransactions = dataTableService.formatLigneToCompteTransaction(donnesWithoutLastLigne);
        if (compteTransactions != null && !compteTransactions.isEmpty()) {
            montantTotal = compteTransactionService.testMontant(compteTransactions);
            if (montantTotal < 0) {
                JOptionPane.showMessageDialog(null, "Solder la transaction avant l' enregistrement !", "Erreur", JOptionPane.ERROR_MESSAGE);
            } else {
                Session.updateAttribute(this, "TransactionView");
                System.out.println(compteTransactions);
                confirmation.setVisible(true);

            }
        } else {
            JOptionPane.showMessageDialog(null, "remplir les comptes d'abord", "Info", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

