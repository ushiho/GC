    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        CompteTransaction compteTransaction = getParam();
        System.out.println(compteTransaction);
        if (compteTransaction == null) {
            JOptionPane.showMessageDialog(null, "Tous les champs sont nécissaires !", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            if (jButton2.getText().equals("Ajouter")) {
                DataTable ligneTable = dataTableService.add(compteTransaction);
                lastLigne(ligneTable);
                dataTableHelper.setList(donnes);
            }
            if (jButton2.getText().equals("Enregistrer")) {
                DataTable ligneModifier = dataTableService.add(compteTransaction);
                lastLigne(ligneModifier);
                dataTableHelper.setList(donnes);
                jButton2.setText("Ajouter");
                jLabel5.setText("Enregistrement");
            }
            jComboBox3.setSelectedIndex(0);
            initComboBox();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

