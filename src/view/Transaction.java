    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (modifyLigneOfTheTable() == 1) {
            DataTable lastligne = donnes.get(donnes.size() - 1);

            jLabel5.setText("Modification");
            jLabel5.setForeground(Color.red);
            DataTable ligneSelected = dataTableHelper.getSelected();
            if (ligneSelected.getType() == 1) {
                jTextField1.setText("" + ligneSelected.getMontantCredit());
                jComboBox1.setSelectedItem("Credit");
                lastligne.setMontantCredit(lastligne.getMontantCredit() - ligneSelected.getMontantCredit());

            }
            if (ligneSelected.getType() == 2) {
                lastligne.setMontantDebit(lastligne.getMontantDebit() - ligneSelected.getMontantDebit());
                jTextField1.setText("" + ligneSelected.getMontantDebit());
                jComboBox1.setSelectedItem("Debit");
            }
            char num[] = ligneSelected.getNumCompte().toCharArray();
            String numCompte = "";
            for (int i = 2; i < num.length; i++) {
                numCompte += num[i];
            }
            Compte compte = compteServices.findCompteByNum(new Integer(numCompte));
            donnes.remove(ligneSelected);
            rempliCombBox(compte, ligneSelected.getEntreprise());
            jComboBox2.setSelectedItem(ligneSelected.getEntreprise());
            jButton2.setText("Enregistrer");

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        DataTable ligneSelected = dataTableHelper.getSelected();
        if (ligneSelected == null) {
            JOptionPane.showMessageDialog(null, "aucune ligne sélectionnée", "Info", JOptionPane.WARNING_MESSAGE);
        } else if (ligneSelected != donnes.get(donnes.size() - 1)) {
            dataTableHelper.remove(ligneSelected);
            donnes.remove(ligneSelected);
            DataTable lasttligne = donnes.get(donnes.size() - 1);
            if (ligneSelected.getType() == 1) {
                lasttligne.setMontantCredit(lasttligne.getMontantCredit() - ligneSelected.getMontantCredit());
            }
            if (ligneSelected.getType() == 2) {
                lasttligne.setMontantDebit(lasttligne.getMontantDebit() - ligneSelected.getMontantDebit());
            }
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

